.model small
.stack
.data
	cad db 50 dup(?)
	
.code
;INICIO
principal proc far
MOV AX,@DATA
MOV DS,AX

;LIMPIA PANTALLA
MOV AL,3
MOV AH,0
INT 10H

;PIDE POR TECLADO
MOV SI,0
CICLOE:
MOV AH,1
INT 21H
CMP AL,13D
JE SIGNOP
CMP AL,45H
JE INCRE
CMP AL,65H
JE INCRE
MOV CAD[SI],AL

INC SI
INCRE:
JMP CICLOE

SIGNOP:
MOV CAD[SI],24H
MOV AX,0
MOV AL,24H
PUSH AX
MOV SI,0

CICLO2:
CMP CAD[SI],24H
JE IMPRIME
MOV AL,CAD[SI]
PUSH AX
INC SI
JMP CICLO2
 
IMPRIME:
POP DX
CMP DL,24H
JE FIN
MOV AH,0EH
MOV AL,DL
INT 10H
JMP IMPRIME





FIN:
MOV AH,4CH
INT 21H

;FIN
principal endp
end principal